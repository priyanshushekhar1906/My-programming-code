// Primitive data types
// string
let name="chotu";
console.log("My name is " +name);
console.log("My data type is "+(typeof name));
// Numbers 
let marks=34;
console.log("My data type is "+(typeof marks));
// Boolean
let isDriver=true;
console.log("My data type is "+(typeof isDriver));
// NULL
let nullVar=null;
console.log("My data type is "+(typeof nullVar));
//  Undefine

let undef=undefined;
console.log("My data type is "+(typeof undef));

// Reference data types

// Arrays
let myarr=[12,23,45,54,false,"chotu"];
console.log("My data type is "+(typeof myarr));

